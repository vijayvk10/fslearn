var a = 10
var b = 10

if(a==b)
{
    console.log("both values are same")
}
else{
    console.log("both values are not same")
}

//arithmetic operators
add=a+b
console.log(add)
sub=a-b
console.log(sub)
mul=a*b
console.log(mul)
div=a/b
console.log(div)
mod=a%b
console.log(mod)

//assignment operators
b += 10; 
console.log(b); 

//comparison operators
console.log(a > b); 
console.log(a == b); 
console.log(a === b); 

//logical operators
let x = true, y = false;
console.log(x && y); 
console.log(x || y); 
console.log(!x);  

//unary operators
console.log(++b); 
console.log(--b); 


